#include <stdio.h>

int main(void)
{

//相等运算符
if ((3!=2)==0)  
       printf("3!=2 is 0\n");	
else if ((3!=2)==1)  
       printf("3!=2 is 1 \n");	

if ((3==2)==0)  
       printf("3==2 is 0\n");	
else if ((3==2)==1)  
       printf("3==2 is 1 \n");	

//关系运算符
if ((3>=2)==0)  
       printf("3>=2 is 0\n");	
else if ((3>=2)==1)  
       printf("3>=2 is 1 \n");	

if ((3>2)==0)  
       printf("3> 2 is 0\n");	
else if ((3>2)==1)  
       printf("3> 2 is 1 \n");	

if ((3<=2)==0)  
       printf("3<=2 is 0\n");	
else if ((3<=2)==1)  
       printf("3<=2 is 1 \n");	

if ((3<2)==0)  
       printf("3< 2 is 0\n");	
else if ((3<2)==1)  
       printf("3< 2 is 1 \n");	
//
return 0;
}
